function parseNametable(str) {
    let newStr = str.replace(/[\[\]\s\,\$\.'"]+/g, "");
    const result = [];
    for (let i = 0; i < newStr.length; i += 2) {
        let entry = newStr.substring(i, i + 2);
        let parse = parseInt(entry, 16);
        result.push(parse);
    }

    if (result.length < 959) {
        let remainder = 959 - result.length;
        for (let i=0; i<remainder; i++) {
            result.push(0);
        } 
    }
    return result;
}

function createDisplayString(nametable){
    let dispNTstr = '';
    for(i=0;i<960;i+=32){
        for(j=0;j<32;j++){
            dispNTstr = dispNTstr + nametable[(j+i)].toString().padStart(2,0).toUpperCase() + ',';
        }
        dispNTstr = dispNTstr + '<br>';
    }
    return dispNTstr;
}

